package cn.zeffect.apk.jump.myapplication2.unit38;

public class Video {
    private double koid;
    private String koname;
    private String videoid;
    private String videoName;

    public String getVideoName() {
        return videoName;
    }

    public Video setVideoName(String videoName) {
        this.videoName = videoName;
        return this;
    }

    public double getKoid() {
        return koid;
    }

    public Video setKoid(double koid) {
        this.koid = koid;
        return this;
    }

    public String getKoname() {
        return koname;
    }

    public Video setKoname(String koname) {
        this.koname = koname;
        return this;
    }

    public String getVideoid() {
        return videoid;
    }

    public Video setVideoid(String videoid) {
        this.videoid = videoid;
        return this;
    }
}
